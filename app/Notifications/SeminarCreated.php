    } */

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            'module_id' =>  $this->seminarData['id'],
            'module_name' =>  'seminar',
            'message' => config('constants.seminar_notification_message')
